package de.rooehler.rastertheque.core;

public class Rectangle {
	
	public int srcX;
	public int srcY;
	
	public int width;
	public int height;
	
	public Rectangle(int srcX, int srcY, int width, int height) {
		
		this.srcX = srcX;
		this.srcY = srcY;
		this.width = width;
		this.height = height;
	}

}
