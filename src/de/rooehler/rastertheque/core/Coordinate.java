package de.rooehler.rastertheque.core;

public class Coordinate {

	
	private double x;
	private double y;
	
	public Coordinate(double pX, double pY){
		
		this.x = pX;
		this.y = pY;
	}

	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public double getY() {
		return y;
	}
	
}
